// Built-in dictionaries. All other locales come from the AI translation cache
// (see src/i18n/translate.ts). English is the canonical source of truth.
const DICT: Record<string, Dict> = { en, ru };

export const EN_DICT = en;
export const BUILTIN_LOCALES = new Set(Object.keys(DICT));

export const i18n = {
  get: () => appStore.get().lang,
  set: (l: Lang) => appStore.setLang(l),
  subscribe: appStore.subscribe,
};

function format(str: string, vars?: Record<string, string | number>) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : `{${k}}`));
}

export function translate(l: Lang, key: string, vars?: Record<string, string | number>): string {
  // Priority: built-in dict for locale → AI translation cache → English → key.
  const builtin = DICT[l]?.[key];
  if (builtin !== undefined) return format(builtin, vars);
  const cached = appStore.get().translations[l]?.[key];
  if (cached !== undefined) return format(cached, vars);
  const fallback = DICT[FALLBACK][key] ?? key;
  return format(fallback, vars);
}

export function useLang(): Lang {
  return useAppStore((s) => s.lang);
}

export function useT() {
  // Subscribe both to the active language and to the translation cache so
  // strings re-render the moment AI translations stream in.
  const l = useLang();
  useAppStore((s) => s.translations[l]);
  return (key: string, vars?: Record<string, string | number>) => translate(l, key, vars);
}

// Public alias matching the requested API name.
export const useTranslation = useT;
